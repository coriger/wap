package com.ruyicai.wap.bean;

public class CardMap {
	private String cardTypeId, platformId, accessType, implClassname;
	private int feerate;
	private String targetcardId, cardDesc, cardName;
	private int state;

	public String getCardTypeId() {
		return cardTypeId;
	}

	public void setCardTypeId(String cardTypeId) {
		this.cardTypeId = cardTypeId;
	}

	public String getPlatformId() {
		return platformId;
	}

	public void setPlatformId(String platformId) {
		this.platformId = platformId;
	}

	public String getAccessType() {
		return accessType;
	}

	public void setAccessType(String accessType) {
		this.accessType = accessType;
	}

	public String getImplClassname() {
		return implClassname;
	}

	public void setImplClassname(String implClassname) {
		this.implClassname = implClassname;
	}

	public int getFeerate() {
		return feerate;
	}

	public void setFeerate(int feerate) {
		this.feerate = feerate;
	}

	public String getTargetcardId() {
		return targetcardId;
	}

	public void setTargetcardId(String targetcardId) {
		this.targetcardId = targetcardId;
	}

	public String getCardDesc() {
		return cardDesc;
	}

	public void setCardDesc(String cardDesc) {
		this.cardDesc = cardDesc;
	}

	public String getCardName() {
		return cardName;
	}

	public void setCardName(String cardName) {
		this.cardName = cardName;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}
}
